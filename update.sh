#!/usr/bin/env bash
# =============================================================================
# AVEX — تحديث وإعادة تشغيل كامل
# =============================================================================
# هذا السكريبت يقوم بـ:
# 1. سحب آخر تحديث من GitHub
# 2. قتل كل العمليات القديمة (Vite + Backend)
# 3. إعادة تشغيل الباكند
#
# الاستخدام:
#   ./update.sh
# =============================================================================

set -e

echo "================================================"
echo "  AVEX — تحديث وإعادة تشغيل"
echo "================================================"
echo ""

# 1. Kill EVERYTHING
echo "🧹 إيقاف كل العمليات القديمة..."
pkill -f "vite" 2>/dev/null || true
pkill -f "avex-server" 2>/dev/null || true
pkill -f "avex-worker" 2>/dev/null || true
pkill -f "node.*vite" 2>/dev/null || true
sleep 3
echo "✅ تم إيقاف كل العمليات"
echo ""

# 2. Pull latest
echo "📥 سحب آخر تحديث..."
git pull origin main
echo ""

# 3. Start backend
echo "🚀 تشغيل الباكند..."
./run.sh
